package com.kenden.kendenrmapp.models;

public class Book {
    public String dTitle, dGenre, dYear, dOrg, dTown, dVol, dType, dSize, dAftor, dCopy, diN, dPlace, dPrice, dSys, dSignAftor, dkS, dMixing;

    public Book() {
    }

    public Book(String dTitle, String dGenre, String dYear, String dOrg, String dTown, String dVol, String dType, String dSize, String dAftor, String dCopy, String diN, String dPlace, String dPrice, String dSys, String dSignAftor, String dkS, String dMixing) {
        this.dTitle = dTitle;
        this.dGenre = dGenre;
        this.dYear = dYear;
        this.dOrg = dOrg;
        this.dTown = dTown;
        this.dVol = dVol;
        this.dType = dType;
        this.dSize = dSize;
        this.dAftor = dAftor;
        this.dCopy = dCopy;
        this.diN = diN;
        this.dPlace = dPlace;
        this.dPrice = dPrice;
        this.dSys = dSys;
        this.dSignAftor = dSignAftor;
        this.dkS = dkS;
        this.dMixing = dMixing;
    }
}
